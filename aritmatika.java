package binaryoperator;

public class Artimatika {
    public static void main(String[] args) {
        int operand1 = 20;
        int operand2 = 30;

        System.out.println(operand1 + operand2); //90
        System.out.println(operand1 - operand2); //30
        System.out.println(operand1 * operand2); //1000
        System.out.println(operand1 / operand2); //2
        System.out.println(operand1 % operand2);
    }

}